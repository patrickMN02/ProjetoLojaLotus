
package Objetos;


public class Vendedor extends Cliente {
    private String nome;
    private String cpf;
    
    public Vendedor(){}

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }
    
    
}



